function usekW = useKW(workbookFile,sheetName,startRow,endRow)
%IMPORTFILE Import data from a spreadsheet
%   usekWH1 = IMPORTFILE(FILE) reads data from the first worksheet in the
%   Microsoft Excel spreadsheet file named FILE and returns the data as
%   column vectors.
%
%   usekWH1 = IMPORTFILE(FILE,SHEET) reads from the specified worksheet.
%
%   usekWH1 = IMPORTFILE(FILE,SHEET,STARTROW,ENDROW) reads from the
%   specified worksheet for the specified row interval(s). Specify STARTROW
%   and ENDROW as a pair of scalars or vectors of matching size for
%   dis-contiguous row intervals. To read to the end of the file specify an
%   ENDROW of inf.%
% Example:
%   usekWH1 = importfile('Homes 01-10_15min_2012-0819-0825 .xlsx','Home
%   01',673,673);
%
%   See also XLSREAD.

% Auto-generated by MATLAB on 2013/10/07 13:03:35

%% Input handling

% If no sheet is specified, read first sheet
if nargin == 1 || isempty(sheetName)
    sheetName = 1;
end

% If row start and end points are not specified, define defaults
if nargin <= 3
    startRow = 673;
    endRow = 673;
end

%% Import the data
data = xlsread(workbookFile, sheetName, sprintf('B%d:B%d',startRow(1),endRow(1)));
for block=2:length(startRow)
    tmpDataBlock = xlsread(workbookFile, sheetName, sprintf('B%d:B%d',startRow(block),endRow(block)));
    data = [data;tmpDataBlock]; %#ok<AGROW>
end

%% Allocate imported array to column variable names
usekW = data(:,1);

